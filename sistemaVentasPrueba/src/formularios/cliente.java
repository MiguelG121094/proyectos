    private void cargarGrilla(){
            //el DefaultTableModel es una libreria para trabajar en el modelo de nuestra grilla
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            String vecContenido[] = new String[3];
        try {
            statement = con.conexion.createStatement();
            resulSet = statement.executeQuery("select * from cliente");
            while(resulSet.next()){
                vecContenido[0] = resulSet.getString("ruc");
                vecContenido[1] = resulSet.getString("nombre");
                vecContenido[2] = resulSet.getString("direccion");
                modelo.addRow(vecContenido);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
